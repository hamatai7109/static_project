public class PracticeWhile {
    public static void main(String[] args) {
//01
// while 文を使ってコンソールに出力して下さい。条件は以下です。
//
//    Java ファイル PracticeWhile を作成して、その中に記述して下さい。
//    100 から 1 ずつ減算して、0 まで表示するプログラムを作成して下さい。
        int num = 100;
        while (num >= 0){
            System.out.println(num);
            num -= 1;
        }
    }
}
